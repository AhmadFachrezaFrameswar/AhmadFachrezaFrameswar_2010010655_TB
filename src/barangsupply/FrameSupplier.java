    private void TableSupplyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableSupplyMouseClicked
    int baris = TableSupply.rowAtPoint(evt.getPoint());       
        String ID = TableSupply.getValueAt(baris, 0).toString();
        tbSupply.setText(ID);
        
        String Nama = TableSupply.getValueAt(baris, 1).toString();
        tbNamaSupply.setText(Nama);
        
        String Alamat = TableSupply.getValueAt(baris, 2).toString();
        tbAlamat.setText(Alamat);
    }//GEN-LAST:event_TableSupplyMouseClicked

