    private void Tampilkan_data(){
    
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("id_supplier");
        model.addColumn("nama_supplier");
        model.addColumn("alamat_supplier");
        
        try{
        String sql = "SELECT * FROM supply655";
        java.sql.Connection conn=(Connection)koneksi.configDB();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery(sql);
        
        while (res.next()){
        model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3)});
        
        }
        TableSupply.setModel(model);
        }catch (SQLException e){
            System.out.println("Error : "+ e.getMessage());
        }
    }
